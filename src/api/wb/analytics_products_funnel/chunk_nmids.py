# Задача файла:
# - Разбивает список артикулов (nmIDs) на пачки по N штук.
# - Несмотря на то, что метод products принимает до 1000 nmIds,
#   мы придерживаемся общей стратегии запросов по 20 штук (константа),
#   чтобы упростить контроль нагрузки и повторное использование логики.
# ---------------------------------------------

CHUNK_SIZE = 20  # Константа: используем 20 артикулов в одном запросе


def chunk_nmids(nmids, size=CHUNK_SIZE):
    # nmids: исходный список артикулов WB
    # size: размер пачки (по умолчанию 20, согласно принятой стратегии)
    # Возвращает список списков, где каждый внутренний список имеет длину <= size
    result = []

    # Итерируемся по исходному списку, формируя срезы заданного размера
    for i in range(0, len(nmids or []), size):
        result.append(nmids[i:i + size])

    # Возвращаем готовые пачки артикулов
    return result

