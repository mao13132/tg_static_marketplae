# Задача файла:
# - Разбивает список артикулов (nmIDs) на пачки по N штук.
# - Этот слой отвечает за подготовку входных данных к API,
#   чтобы строго соблюдать ограничение: от 1 до 20 артикулов в одном запросе.
# ---------------------------------------------

CHUNK_SIZE = 20  # Константа: максимум 20 артикулов в одном запросе


def chunk_nmids(nmids, size=CHUNK_SIZE):
    # nmids: исходный список артикулов WB
    # size: размер пачки (по умолчанию 20, согласно ограничению API)
    # Возвращает список списков, где каждый внутренний список имеет длину <= size
    result = []

    # Проходим по исходному списку, шагом size
    for i in range(0, len(nmids or []), size):
        # Берем срез нужного размера и добавляем в результат
        result.append(nmids[i:i + size])

    # Возвращаем готовые пачки артикулов
    return result

